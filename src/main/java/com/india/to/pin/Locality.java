package main.java.com.india.to.pin;

import java.util.Date;

public class Locality {
	long id;
	Date timestamp;
	String stateName;
	String districtName;
	String subdistname;
	String villagename;
	String localitydetail1;
	String localitydetail2;
	String localitydetail3;
	String officeName;
	String pincode;
	
	public long getId() {
		return id;
	}
	public void setId(long id) {
		this.id = id;
	}
	public Date getTimestamp() {
		return timestamp;
	}
	public void setTimestamp(Date timestamp) {
		this.timestamp = timestamp;
	}
	public String getStateName() {
		return stateName;
	}
	public void setStateName(String stateName) {
		this.stateName = stateName;
	}
	public String getDistrictName() {
		return districtName;
	}
	public void setDistrictName(String districtName) {
		this.districtName = districtName;
	}
	public String getSubdistname() {
		return subdistname;
	}
	public void setSubdistname(String subdistname) {
		this.subdistname = subdistname;
	}
	public String getVillagename() {
		return villagename;
	}
	public void setVillagename(String villagename) {
		this.villagename = villagename;
	}
	public String getLocalitydetail1() {
		return localitydetail1;
	}
	public void setLocalitydetail1(String localitydetail1) {
		this.localitydetail1 = localitydetail1;
	}
	public String getLocalitydetail2() {
		return localitydetail2;
	}
	public void setLocalitydetail2(String localitydetail2) {
		this.localitydetail2 = localitydetail2;
	}
	public String getLocalitydetail3() {
		return localitydetail3;
	}
	public void setLocalitydetail3(String localitydetail3) {
		this.localitydetail3 = localitydetail3;
	}
	public String getOfficeName() {
		return officeName;
	}
	public void setOfficeName(String officeName) {
		this.officeName = officeName;
	}
	public String getPincode() {
		return pincode;
	}
	public void setPincode(String pincode) {
		this.pincode = pincode;
	}
	
}
